public class DoublePoint {
    double x;
    double y;
    public DoublePoint(double x, double y) {
        this.x = x;
        this.y = y;

    }

    public double getY() {
        return y;
    }

    public void setY(double y) {
        this.y = y;
    }

    public double getX() {
        return x;
    }

    public void setX(double x) {
        this.x = x;
    }
}
